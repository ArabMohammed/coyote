std::vector<ctxt> VectorProgram::initialize_temps(RuntimeContext &info)
{
    std::vector<ctxt> ts(12);
    ts[0] = encrypt_input("000001111111111111111111111110111111111111111111111011111111111111111111111111011111111111111111111101100111111111111111111111111011111111111111111111101011111111111111111111111001111111111111111111111100000000000000000001111111111111111111111110000000000000000", info);
    ts[1] = encrypt_input("000011001101100000011111100000101011000101110100011100111000000", info);
    ts[2] = encrypt_input("11111101100110111111111000111111111000101111101100101110011100111111111", info);
    ts[5] = encrypt_input("0000000000000000000000000000000000011000000000000000000", info);
    return ts;
}

ctxt VectorProgram::computation(std::vector<ctxt> ts, std::map<std::string, ptxt> bits, RuntimeContext &info)
{
    seal::RelinKeys rk = info.keys->rk;
    seal::GaloisKeys gk = info.keys->gk;

    ctxt vs[6];
    ctxt ss[28];

    vs[0] = ts[0];
    info.eval->rotate_rows(vs[0], -32, gk, ss[0]); // __s0 = __v0 >> 32
    info.eval->rotate_rows(vs[0], -20, gk, ss[1]); // __s1 = __v0 >> 20
    info.eval->rotate_rows(vs[0], -41, gk, ss[2]); // __s2 = __v0 >> 41
    info.eval->rotate_rows(vs[0], -28, gk, ss[3]); // __s3 = __v0 >> 28
    info.eval->rotate_rows(vs[0], -1, gk, ss[4]); // __s4 = __v0 >> 1
    info.eval->rotate_rows(vs[0], -4, gk, ss[5]); // __s5 = __v0 >> 4
    info.eval->rotate_rows(vs[0], -39, gk, ss[6]); // __s6 = __v0 >> 39
    info.eval->rotate_rows(vs[0], -16, gk, ss[7]); // __s7 = __v0 >> 16
    
    // __t3 = blend(__v0@000001000000010010000000000000000000000000000000000000, __s4@000000000100000000000000000000000000000000000000000000, __s5@000000000000100000001000000000000000000000000000000000, __s7@000000000000000000000000100000000000000000000000000001, __s0@000000000000000000000000000000000000010100000000001000, __s2@000000000000000000000000000000000000000000000000110100, __s6@000000000000000000000000000000000000000000000000000010, __t1@111110111011001101110111010111111111101011111111000000)
    {
    ctxt t3_1, t3_2, t3_3, t3_4, t3_5, t3_6, t3_7;
    info.eval->multiply_plain(vs[0], bits["000001000000010010000000000000000000000000000000000000"], t3_1);
    info.eval->multiply_plain(ss[4], bits["000000000100000000000000000000000000000000000000000000"], t3_2);
    info.eval->multiply_plain(ss[5], bits["000000000000100000001000000000000000000000000000000000"], t3_3);
    info.eval->multiply_plain(ss[7], bits["000000000000000000000000100000000000000000000000000001"], t3_4);
    info.eval->multiply_plain(ss[0], bits["000000000000000000000000000000000000010100000000001000"], t3_5);
    info.eval->multiply_plain(ss[2], bits["000000000000000000000000000000000000000000000000110100"], t3_6);
    info.eval->multiply_plain(ss[6], bits["000000000000000000000000000000000000000000000000000010"], t3_7);
    info.eval->add_many({t3_1, t3_2, t3_3, t3_4, t3_5, t3_6, t3_7, ts[1]}, ts[3]);
    }
    
    
    // __t4 = blend(__v0@000000011000000000000000000000000000000000000000000000, __s4@000000000000000001010000000000000000001000000000000000, __s1@000000000000000000000000010000000100000000000000000000, __s7@000000000000000000000000000001000000000000000000000000, __s3@000000000000000000000000000000000001000000010000000000, __s0@000000000000000000000000000000000000000000100000000000, __s6@000000000000000000000000000000000000000000000001000000, __t2@111111100111111110101111100110111010110111001110111111)
    {
    ctxt t4_1, t4_2, t4_3, t4_4, t4_5, t4_6, t4_7;
    info.eval->multiply_plain(vs[0], bits["000000011000000000000000000000000000000000000000000000"], t4_1);
    info.eval->multiply_plain(ss[4], bits["000000000000000001010000000000000000001000000000000000"], t4_2);
    info.eval->multiply_plain(ss[1], bits["000000000000000000000000010000000100000000000000000000"], t4_3);
    info.eval->multiply_plain(ss[7], bits["000000000000000000000000000001000000000000000000000000"], t4_4);
    info.eval->multiply_plain(ss[3], bits["000000000000000000000000000000000001000000010000000000"], t4_5);
    info.eval->multiply_plain(ss[0], bits["000000000000000000000000000000000000000000100000000000"], t4_6);
    info.eval->multiply_plain(ss[6], bits["000000000000000000000000000000000000000000000001000000"], t4_7);
    info.eval->add_many({t4_1, t4_2, t4_3, t4_4, t4_5, t4_6, t4_7, ts[2]}, ts[4]);
    }
    
    info.eval->multiply(ts[3], ts[4], vs[1]); // __v1 = __t3 * __t4
    info.eval->relinearize_inplace(vs[1], rk);
    info.eval->rotate_rows(vs[1], -22, gk, ss[8]); // __s8 = __v1 >> 22
    info.eval->rotate_rows(vs[1], -53, gk, ss[9]); // __s9 = __v1 >> 53
    info.eval->rotate_rows(vs[1], -4, gk, ss[10]); // __s10 = __v1 >> 4
    info.eval->rotate_rows(vs[1], -1, gk, ss[11]); // __s11 = __v1 >> 1
    info.eval->rotate_rows(vs[1], -2, gk, ss[12]); // __s12 = __v1 >> 2
    info.eval->rotate_rows(vs[1], -7, gk, ss[13]); // __s13 = __v1 >> 7
    info.eval->rotate_rows(vs[1], -39, gk, ss[14]); // __s14 = __v1 >> 39
    info.eval->rotate_rows(vs[1], -41, gk, ss[15]); // __s15 = __v1 >> 41
    info.eval->rotate_rows(vs[1], -38, gk, ss[16]); // __s16 = __v1 >> 38
    info.eval->rotate_rows(vs[1], -34, gk, ss[17]); // __s17 = __v1 >> 34
    info.eval->multiply(ss[1], ts[5], vs[2]); // __v2 = __s1 * __t5
    info.eval->relinearize_inplace(vs[2], rk);
    
    // __t6 = blend(__v1@001000000010000000000000000000000000000000000000100000, __s14@000001000000000000010000000001000000000000000000000000, __s10@000000001000000000000000000000000000000000000000000000, __s17@000000000001000000000000000000000000000000000000000000, __s16@000000000000001000000000000000000000000000000000000000, __s15@000000000000000010000000000000000000010100000000000000, __s12@000000000000000001000000000000000000000000000000000000, __s9@000000000000000000100000000000000010000000100010000000, __s8@000000000000000000000010000000010100001010000000000000, __s11@000000000000000000000001010000000000000000000000000000, __s13@000000000000000000000000100010000000000000000000000000, __v2@000000000000000000000000000000000001000000000000000000)
    {
    ctxt t6_1, t6_2, t6_3, t6_4, t6_5, t6_6, t6_7, t6_8, t6_9, t6_10, t6_11, t6_12;
    info.eval->multiply_plain(vs[1], bits["001000000010000000000000000000000000000000000000100000"], t6_1);
    info.eval->multiply_plain(ss[14], bits["000001000000000000010000000001000000000000000000000000"], t6_2);
    info.eval->multiply_plain(ss[10], bits["000000001000000000000000000000000000000000000000000000"], t6_3);
    info.eval->multiply_plain(ss[17], bits["000000000001000000000000000000000000000000000000000000"], t6_4);
    info.eval->multiply_plain(ss[16], bits["000000000000001000000000000000000000000000000000000000"], t6_5);
    info.eval->multiply_plain(ss[15], bits["000000000000000010000000000000000000010100000000000000"], t6_6);
    info.eval->multiply_plain(ss[12], bits["000000000000000001000000000000000000000000000000000000"], t6_7);
    info.eval->multiply_plain(ss[9], bits["000000000000000000100000000000000010000000100010000000"], t6_8);
    info.eval->multiply_plain(ss[8], bits["000000000000000000000010000000010100001010000000000000"], t6_9);
    info.eval->multiply_plain(ss[11], bits["000000000000000000000001010000000000000000000000000000"], t6_10);
    info.eval->multiply_plain(ss[13], bits["000000000000000000000000100010000000000000000000000000"], t6_11);
    info.eval->multiply_plain(vs[2], bits["000000000000000000000000000000000001000000000000000000"], t6_12);
    info.eval->add_many({t6_1, t6_2, t6_3, t6_4, t6_5, t6_6, t6_7, t6_8, t6_9, t6_10, t6_11, t6_12}, ts[6]);
    }
    
    
    // __t7 = blend(__s9@001000000001000000000010100000010000000100000000000000, __v1@000001000000001000000000000000000000000000100010000000, __s11@000000001000000000000000000001000000001000000000000000, __s12@000000000010000000000000000000000000000000000000000000, __s17@000000000000000010110000010000000000000000000000000000, __s10@000000000000000001000000000000000000000000000000000000, __s8@000000000000000000000001000010000000000000000000000000, __s16@000000000000000000000000000000000101010000000000000000, __s13@000000000000000000000000000000000010000010000000100000)
    {
    ctxt t7_1, t7_2, t7_3, t7_4, t7_5, t7_6, t7_7, t7_8, t7_9;
    info.eval->multiply_plain(ss[9], bits["001000000001000000000010100000010000000100000000000000"], t7_1);
    info.eval->multiply_plain(vs[1], bits["000001000000001000000000000000000000000000100010000000"], t7_2);
    info.eval->multiply_plain(ss[11], bits["000000001000000000000000000001000000001000000000000000"], t7_3);
    info.eval->multiply_plain(ss[12], bits["000000000010000000000000000000000000000000000000000000"], t7_4);
    info.eval->multiply_plain(ss[17], bits["000000000000000010110000010000000000000000000000000000"], t7_5);
    info.eval->multiply_plain(ss[10], bits["000000000000000001000000000000000000000000000000000000"], t7_6);
    info.eval->multiply_plain(ss[8], bits["000000000000000000000001000010000000000000000000000000"], t7_7);
    info.eval->multiply_plain(ss[16], bits["000000000000000000000000000000000101010000000000000000"], t7_8);
    info.eval->multiply_plain(ss[13], bits["000000000000000000000000000000000010000010000000100000"], t7_9);
    info.eval->add_many({t7_1, t7_2, t7_3, t7_4, t7_5, t7_6, t7_7, t7_8, t7_9}, ts[7]);
    }
    
    info.eval->add(ts[6], ts[7], vs[3]); // __v3 = __t6 + __t7
    info.eval->rotate_rows(vs[3], -8, gk, ss[18]); // __s18 = __v3 >> 8
    info.eval->rotate_rows(vs[3], -6, gk, ss[19]); // __s19 = __v3 >> 6
    info.eval->rotate_rows(vs[3], -17, gk, ss[20]); // __s20 = __v3 >> 17
    info.eval->rotate_rows(vs[3], -2, gk, ss[21]); // __s21 = __v3 >> 2
    info.eval->rotate_rows(vs[3], -47, gk, ss[22]); // __s22 = __v3 >> 47
    info.eval->rotate_rows(vs[3], -38, gk, ss[23]); // __s23 = __v3 >> 38
    info.eval->rotate_rows(vs[3], -51, gk, ss[24]); // __s24 = __v3 >> 51
    
    // __t8 = blend(__s23@000000000100000000000000000000000000000000000000000000, __s19@000000000001000000000000000000000000000000000000000000, __s22@000000000000100000000000000000000000000000000000000000, __s21@000000000000000000000000010000010000000000000000000000, __s24@000000000000000000000000000000001000000000000000000000, __v3@000000000000000000000000000000000100000000000000100000, __s20@000000000000000000000000000000000000000100000000000000)
    {
    ctxt t8_1, t8_2, t8_3, t8_4, t8_5, t8_6, t8_7;
    info.eval->multiply_plain(ss[23], bits["000000000100000000000000000000000000000000000000000000"], t8_1);
    info.eval->multiply_plain(ss[19], bits["000000000001000000000000000000000000000000000000000000"], t8_2);
    info.eval->multiply_plain(ss[22], bits["000000000000100000000000000000000000000000000000000000"], t8_3);
    info.eval->multiply_plain(ss[21], bits["000000000000000000000000010000010000000000000000000000"], t8_4);
    info.eval->multiply_plain(ss[24], bits["000000000000000000000000000000001000000000000000000000"], t8_5);
    info.eval->multiply_plain(vs[3], bits["000000000000000000000000000000000100000000000000100000"], t8_6);
    info.eval->multiply_plain(ss[20], bits["000000000000000000000000000000000000000100000000000000"], t8_7);
    info.eval->add_many({t8_1, t8_2, t8_3, t8_4, t8_5, t8_6, t8_7}, ts[8]);
    }
    
    
    // __t9 = blend(__s22@000000000101000000000000000000000100000000000000000000, __s21@000000000000100000000000000000000000000000000000100000, __s20@000000000000000000000000010000000000000000000000000000, __s24@000000000000000000000000000000010000000100000000000000, __s18@000000000000000000000000000000001000000000000000000000)
    {
    ctxt t9_1, t9_2, t9_3, t9_4, t9_5;
    info.eval->multiply_plain(ss[22], bits["000000000101000000000000000000000100000000000000000000"], t9_1);
    info.eval->multiply_plain(ss[21], bits["000000000000100000000000000000000000000000000000100000"], t9_2);
    info.eval->multiply_plain(ss[20], bits["000000000000000000000000010000000000000000000000000000"], t9_3);
    info.eval->multiply_plain(ss[24], bits["000000000000000000000000000000010000000100000000000000"], t9_4);
    info.eval->multiply_plain(ss[18], bits["000000000000000000000000000000001000000000000000000000"], t9_5);
    info.eval->add_many({t9_1, t9_2, t9_3, t9_4, t9_5}, ts[9]);
    }
    
    info.eval->add(ts[8], ts[9], vs[4]); // __v4 = __t8 + __t9
    info.eval->rotate_rows(vs[4], -5, gk, ss[25]); // __s25 = __v4 >> 5
    info.eval->rotate_rows(vs[4], -53, gk, ss[26]); // __s26 = __v4 >> 53
    info.eval->rotate_rows(vs[4], -33, gk, ss[27]); // __s27 = __v4 >> 33
    
    // __t10 = blend(__s27@000010000000100000000000000000000000000000000000000000, __s26@000000000010000000000000000000000000001000000001000000, __s25@000000000000001001000000000000000000010000000000000000, __v4@000000000000000000000000000000010000000000000000000000)
    {
    ctxt t10_1, t10_2, t10_3, t10_4;
    info.eval->multiply_plain(ss[27], bits["000010000000100000000000000000000000000000000000000000"], t10_1);
    info.eval->multiply_plain(ss[26], bits["000000000010000000000000000000000000001000000001000000"], t10_2);
    info.eval->multiply_plain(ss[25], bits["000000000000001001000000000000000000010000000000000000"], t10_3);
    info.eval->multiply_plain(vs[4], bits["000000000000000000000000000000010000000000000000000000"], t10_4);
    info.eval->add_many({t10_1, t10_2, t10_3, t10_4}, ts[10]);
    }
    
    
    // __t11 = blend(__s22@000010000000000000000000000000000000000000000000000000, __s18@000000000010000000000000000000000000000000000001000000, __s23@000000000000100000000000000000000000000000000000000000, __v3@000000000000001001000000000000010000011000000000000000)
    {
    ctxt t11_1, t11_2, t11_3, t11_4;
    info.eval->multiply_plain(ss[22], bits["000010000000000000000000000000000000000000000000000000"], t11_1);
    info.eval->multiply_plain(ss[18], bits["000000000010000000000000000000000000000000000001000000"], t11_2);
    info.eval->multiply_plain(ss[23], bits["000000000000100000000000000000000000000000000000000000"], t11_3);
    info.eval->multiply_plain(vs[3], bits["000000000000001001000000000000010000011000000000000000"], t11_4);
    info.eval->add_many({t11_1, t11_2, t11_3, t11_4}, ts[11]);
    }
    
    info.eval->add(ts[10], ts[11], vs[5]); // __v5 = __t10 + __t11
    return vs[5];
}
    