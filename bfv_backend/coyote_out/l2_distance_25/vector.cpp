    seal::GaloisKeys gk = info.keys->gk;

    ctxt vs[11];
    ctxt ss[54];

    vs[0] = ts[0];
    info.eval->rotate_rows(vs[0], -6, gk, ss[0]); // __s0 = __v0 >> 6
    info.eval->rotate_rows(vs[0], -8, gk, ss[1]); // __s1 = __v0 >> 8
    info.eval->rotate_rows(vs[0], -5, gk, ss[2]); // __s2 = __v0 >> 5
    info.eval->rotate_rows(vs[0], -2, gk, ss[3]); // __s3 = __v0 >> 2
    info.eval->rotate_rows(vs[0], -17, gk, ss[4]); // __s4 = __v0 >> 17
